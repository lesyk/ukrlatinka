  def convert(str, direct)
    alfabet = {}.merge(DOWNCHAR).merge(UPCHAR)

    if direct.to_s == 'uk_lat'
      alfabet.each do |uk, lat|
        str.gsub!(uk, lat)
      end
    end

    if direct.to_s == 'lat_uk'
      alfabet.each do |uk, lat|
        str.gsub!(lat, uk)
      end
    end

    str
  end
